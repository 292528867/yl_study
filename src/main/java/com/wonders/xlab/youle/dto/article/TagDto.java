public class TagDto {

    private String name;

    private Double tagX;

    private Double tagY;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getTagX() {
        return tagX;
    }

    public void setTagX(Double tagX) {
        this.tagX = tagX;
    }

    public Double getTagY() {
        return tagY;
    }

    public void setTagY(Double tagY) {
        this.tagY = tagY;
    }
}
